console.log(21 > 20);

// Strict equality.
// It will return true only if both datatype and data are same.
console.log(21 === 21);

// Loose equality.
// It will return true, if data are equal.
// Loose equality can lead to some unexpected results.
console.log('21' == 21);

// Other scenario.
let age = 20;
// age variable will be updated in this scenario.
console.log(age = 21);
console.log(age);