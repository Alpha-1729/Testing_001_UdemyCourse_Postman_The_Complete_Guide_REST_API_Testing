/*
 * Freestyle Jobs:
 *   - Created through the web interface by configuring a series of steps.
 *   - Legacy way of defining jobs in Jenkins.
 *   - Good for beginners and simple build processes.
 *
 * Declarative Pipeline (Jenkinsfile):
 *   - Modern way of defining Jenkins jobs.
 *   - Uses Jenkins DSL(Domain Specific Language) stored as a text file in the source code repository.
 *
 * Advantages of Jenkinsfile:
 *   - Pipeline script can be stored in version control.
 *   - More powerful language that supports complex configurations.
 */