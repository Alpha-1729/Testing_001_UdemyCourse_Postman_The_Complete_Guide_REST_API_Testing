const numbers = [23, 45, 25, 65];

// Adding new numbers at the end of the array.
numbers.push(100);

// Get the size of the array.
console.log(numbers.length);

// Remove the last item in the array.
numbers.pop();

// Getting the last item in the array and remove the last item in the array.
console.log(numbers.pop());

// Get the first item in the array and remove the first item in the array.
console.log(numbers.shift());


